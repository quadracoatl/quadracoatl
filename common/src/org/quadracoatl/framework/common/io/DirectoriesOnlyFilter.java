package org.quadracoatl.framework.common.io;

import java.io.IOException;
import java.nio.file.DirectoryStream;
import java.nio.file.Files;
import java.nio.file.LinkOption;
import java.nio.file.Path;

public class DirectoriesOnlyFilter implements DirectoryStream.Filter<Path> {
	public static final DirectoriesOnlyFilter INSTANCE = new DirectoriesOnlyFilter();
	
	public DirectoriesOnlyFilter() {
		super();
	}
	
	@Override
	public boolean accept(Path entry) throws IOException {
		return Files.isDirectory(entry, LinkOption.NOFOLLOW_LINKS);
	}
	
}
